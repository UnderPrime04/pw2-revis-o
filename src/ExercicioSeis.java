
public class ExercicioSeis {

	public static void main(String[] args) {
		double pet = 11;
		
		if (pet >= 10) 
		{
			System.out.println("Peso do pet = " + pet);
			System.out.println("Pet acima do peso!");
		}
		
		else if (pet <= 4) 
		{
			System.out.println("Peso do pet = " + pet);
			System.out.println("Pet abaixo do peso!");
		}
		
		else 
		{
			System.out.println("Peso do pet = " + pet);
			System.out.println("Pet com peso ideal!");
		}
	}
}
